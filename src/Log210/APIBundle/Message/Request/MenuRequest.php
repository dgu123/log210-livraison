<?php

namespace Log210\APIBundle\Message\Request;


class MenuRequest {

    /**
     * @var string $name
     */
    private $name;

    /**
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * @param string $name
     */
    public function setName($name)
    {
        $this->name = $name;
    }
}
