<?php

namespace Log210\LivraisonBundle\ApiResponse;

class RestaurantResponse {
    private $id;
    private $name;
    private $description;
    private $address;
    private $phone;
    private $restaurateur_href;

    /**
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * @param int $id
     */
    public function setId($id)
    {
        $this->id = $id;
    }

    /**
     * @return mixed
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * @param mixed $name
     */
    public function setName($name)
    {
        $this->name = $name;
    }

    /**
     * @return mixed
     */
    public function getDescription()
    {
        return $this->description;
    }

    /**
     * @param mixed $description
     */
    public function setDescription($description)
    {
        $this->description = $description;
    }

    /**
     * @return mixed
     */
    public function getAddress()
    {
        return $this->address;
    }

    /**
     * @param mixed $address
     */
    public function setAddress($address)
    {
        $this->address = $address;
    }

    /**
     * @return mixed
     */
    public function getPhone()
    {
        return $this->phone;
    }

    /**
     * @param mixed $phone
     */
    public function setPhone($phone)
    {
        $this->phone = $phone;
    }

    /**
     * @return mixed
     */
    public function getRestaurateur_href()
    {
        return $this->restaurateur_href;
    }

    /**
     * @param mixed $restaurateur_href
     */
    public function setRestaurateur_href($restaurateur_href)
    {
        $this->restaurateur_href = $restaurateur_href;
    }


}